const locationAPI = {
  getLocation(extraParams) {
    const promise = new Promise((resolve, reject) => {
      const timeout = Math.random() > 0.1 ? 1000 : 2000;

      const callback = () => {
        if (timeout === 1000) {
          let response;

          if (extraParams.place_name) {
            const location = extraParams.place_name.toLowerCase();
            const data = LOCATIONS[location];

            if (data) {
              response = {
                request: {
                  pretty: 1,
                  action: 'search_listings',
                  encoding: 'json',
                  num_res: 20,
                  page: 1,
                  location: data.place_name,
                },
                response: {
                  application_response_code: 100,
                  status_code: 200,
                  total_results: data.total_results,
                  listings: data.listings,
                  locations: [],
                },
              };
            } else {
              response = {
                request: {
                  pretty: 1,
                  action: 'search_listings',
                  encoding: 'json',
                  num_res: 0,
                  page: 1,
                  location: extraParams.place_name,
                },
                response: {
                  application_response_code: 201,
                  status_code: 200,
                  total_results: 0,
                  listings: [],
                  locations: [],
                },
              };
            }
          } else if (extraParams.centre_point) {
            for (const o in LOCATIONS) {
              const location = LOCATIONS[o];
              if (location.centre_point === extraParams.centre_point) {
                response = {
                  request: {
                    pretty: 1,
                    action: 'search_listings',
                    encoding: 'json',
                    num_res: 20,
                    page: 1,
                    location: location.place_name,
                  },
                  response: {
                    application_response_code: 100,
                    status_code: 200,
                    total_results: location.total_results,
                    listings: location.listings,
                    locations: [],
                  },
                };
              }
            }
          }
          resolve(JSON.stringify(response));
        } else {
          reject('Timeout');
        }
      };

      setTimeout(callback, timeout);
    });

    return promise
      .then(response => JSON.parse(response))
      .then(
        ({request, response}) => {
          const appResCode = Number(response.application_response_code);

          if (appResCode === 100 || appResCode === 101 || appResCode === 110) {
            // the query returned a list of properties
            if (response.listings.length) {
              return {
                listings: {
                  currentlyDisplayed: Number(request.num_res),
                  searchTerm: request.location,
                  page: request.page,
                  total: response.total_results,
                  listings: response.listings,
                },
              };
            }
            throw new Error('Zero properties returned');
          }
          if (appResCode === 200 || appResCode === 202) {
            // 200
            // The search term was ambiguous.
            // In this case Nestoria returns a list of suggested locations.
            // 201
            // The specified location is not valid,
            // but has a very similar spelling to one or more locations which are valid.
            // Possible valid locations are returned in the response.
            return {locations: response.locations};
          }
          if (appResCode === 201) {
            // unknown location
            throw new Error('Location not matched');
          }
          // any other response is considered an error
          throw new Error('Other');
        },
        error => {
          throw new Error('Network connection issues / timeout');
        },
      );
  },
};

export default locationAPI;
